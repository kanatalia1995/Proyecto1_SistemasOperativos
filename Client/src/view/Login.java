    private boolean validateUser() throws IOException, ClassNotFoundException, ParseException{
        this.controller = new LoginController();
        this.serverStateText.setText("Server connected");
        String username = fld_username.getText();
        String password = fld_password.getText();
        int bool = this.controller.login(username, password);
        if (bool == 1 ) return true;
        return false;
    }
    
    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        try {
            serverStateText.setText("Validating user.."); 
            if (validateUser()){
                serverStateText.setText("User accepted.."); 
                Interface.controller =  new PlayerController(this.controller.user);
                Interface interfaz = new Interface();
                interfaz.setVisible(true);
                this.setVisible(false);
                serverStateText.setText("Initializing application");     
            }
        } catch (IOException ex) {
            serverStateText.setText("Login failed");
            //Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            serverStateText.setText("Error converting user data");
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

