    private void MutePlayer() {
        float value = getVolume();
        if (value == 0.0){
            if (prev_volume < 0){
                prev_volume = minVolume();
            }
            setVolume(prev_volume);
        }else{
            prev_volume = value;
            setVolume(minVolume());
        }
    }
